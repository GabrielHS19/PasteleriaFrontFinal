    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String tel = telefono.getText();
        boolean isNumeric = tel.matches("[+-]?\\d*(\\.\\d+)?");
        if (comprobar() == false) {
            if (isNumeric == true) {
                try {

                    usuario = new Usuario();
                    usuario.setNombre(nombre.getText());
                    usuario.setApellido(apellido.getText());
                    usuario.setCorreo(correo.getText());
                    usuario.setDireccion(direccion.getText());
                    usuario.setTelefono(telefono.getText());
                    usuario.setPassword(password.getText());
                    JSONObject json = new JSONObject(usuario);
                    REQUEST.save(json.toString());
                    JOptionPane.showMessageDialog(null, "Usuario agregado con exito");
                } catch (Exception ex) {
                    Logger.getLogger(principal.class.getName()).log(Level.SEVERE, null, ex);
                }
                
            } else {
                JOptionPane.showMessageDialog(null, "Favor de ingresar un numero correcto de telefono");
            }
            
        }else{
             JOptionPane.showMessageDialog(null, "Favor de llenar todos los campos");
        }
        limpiar();
        
    }//GEN-LAST:event_jButton1ActionPerformed
    

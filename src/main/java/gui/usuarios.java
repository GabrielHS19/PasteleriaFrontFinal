    public boolean comprobar() {
        boolean resul;
        if (nombre.getText().equals("")
                || apellido.getText().equals("")
                || correo.getText().equals("")
                || direccion.getText().equals("")
                || telefono.getText().equals("")
                || password.getText().equals("")) {
            resul = true;
        } else {
            resul = false;
        }
        return resul;
    }

